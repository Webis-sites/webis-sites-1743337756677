/**
 * פונקציה שמחזירה פרומפטים עיצוביים לפי בחירות המשתמש
 */
export function getStylePrompts(styleIds: string[] = []): string {
  if (!styleIds || styleIds.length === 0) {
    return '';
  }

  // מסנן את הפרומפטים הרלוונטיים לפי ה-ID שנבחרו
  const selectedPrompts = stylePrompts
    .filter(style => styleIds.includes(style.id))
    .map(style => style.prompt.trim());

  // אם נבחרו מספר סגנונות, צור הקדמה שמסבירה שיש לשלב ביניהם
  let combinedPrompt = '';
  
  if (selectedPrompts.length > 1) {
    combinedPrompt += `שלב את הסגנונות הבאים בצורה הרמונית:\n\n`;
  }
  
  combinedPrompt += selectedPrompts.join('\n\n');
  
  return combinedPrompt;
} 